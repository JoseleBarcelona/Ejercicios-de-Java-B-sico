package CursosBootcamp.ConceptosJava;

public class BucleWhile {

    public static void main(String[] args) {
        int count = 0;

        while (count < 10){

            System.out.println(" Neus bonica " + count);
            count++;
            /*(Esto es una condición), con el operador incremento ++ itinera 1 vez tras otra, hasta que se
             cumple la función while*/

        }
        System.out.println("fin");
    }
}
