package CursosBootcamp.ConceptosJava;

public class VariablesTipos {

    public static void main(String[] args) {

        // números enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3 = 3; // 4 byte
        long number4 = 4; // 8 byte

        // números decimales

        float decimal1 = 4.9f; // calcula decimales y se pone la f para ayudar al programa a interpretarlo mejor
        double decimal2 = 5.36; // calcula decimales más exactos

        // caracter

        char caracter1 = 'a'; // un solo caracter

        // booleanos

        boolean verdadero = true;
        boolean falso = false;

        // cadena de textos

        String nombre = "José";
        String apellido = "Núria";

        // tipos envoltorios

        Integer numero = null;
        Long numero2 = 2L; // se pone la L para indicar que es un Long

    }


}
