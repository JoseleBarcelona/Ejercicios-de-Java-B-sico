package CursosBootcamp.TiposDatosAvanzados.String;

public class StringLenght {
    //String es una clase que tiene parámetros y métodos, por lo que se puede usar como objeto
    public static void main(String[] args) {
        String cadena = "Mensaje de texto"; //Se declara parecido a un objeto

        int cadenaLenght = cadena.length();
        System.out.println("\nLa longitud de la cadena de texto es: " + cadenaLenght);
        //cadena.length() llama al método definir toda la longitud del texto
    }
}
